<h1 id="image-to-uml">Image to UML</h1>
<p>
    This project is an assignment for the Software Engineering course at LIACS, Leiden University. The
    objective of the project is to develop tools for the conversion of images of UML class diagrams in
    common graphical formats (e.g., BMP, JPEG, GIF, etc.) to structured UML class diagrams stored e.g.,
    in XMI format to enable their analysis with UML design tools.
<p>
    The assignment was done in a group with <a hreflang="nl" href="http://www.liacs.nl/~tkappe/">Tobias 
    Kapp&eacute;</a> and <a hreflang="nl" href="http://www.liacs.nl/~plouchtc/">Philipe Louchtch</a>.
